#!/bin/perl -w
#
# https://adventofcode.com/2021/day/10

use strict;

print "2021 Day 10\n";
my $input = do { local $/; <DATA> }; # slurp it
my %score = ( 'p1' => 0, 'p2' => 0 );
my %points = ( 'p1' => { ')' => 3, ']' => 57, '}' => 1197, '>' => 25137 },
	 'p2' => { ')' => 1, ']' => 2, '}' => 3, '>' => 4 } );
my %left = ( '(' => ')', '[' => ']', '{' => '}', '<' => '>' );
my %right =  ( ')' => '(', ']' => '[', '}' => '{', '>' => '<' );
my @scores = ();
LINE: foreach my $line (split("\n", $input)) {
	my @open = ();
	foreach my $c (split('', $line)) {
		if (exists $left{$c}) {
			push @open, $c;
		} elsif (exists $right{$c}) {
			if ($open[$#open] eq $right{$c}) {
				pop @open;
			} else {
				$score{'p1'} += $points{'p1'}{$c};
				next LINE;
			}
		} else {
			die "Unknown character $c in input.";
		}
	}
	if (scalar @open) {
		my $this_score = 0;
		while (my $c = pop @open) {
			$this_score *= 5;
			$this_score += $points{'p2'}{$left{$c}};
		}
		push @scores, $this_score;
	}
}
@scores = sort { $a <=> $b } @scores;
$score{'p2'} = $scores[($#scores + 1)/2];

print "Part 1: Total corrupted line score $score{'p1'}\n";
print "Part 2: Total incomplete line score $score{'p2'}\n";

