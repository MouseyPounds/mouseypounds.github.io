#!/bin/perl -w
#
# https://adventofcode.com/2016/day/24
#
# Initially tried a naive BFS that ran around until it hit all the numbers which is fine for the small example but will run out
# of memory on the actual input. So the new approach is using BFS for each number-to-number distance and then going through
# the permutations of the number-to-number paths. We do assume that the numbers go from 0 .. n with nothing skipped.

use strict;
use POSIX;
use List::Util qw(max);
use Math::Combinatorics;

print "2016 Day 24\n";
my $puzzle = do { local $/; <DATA> }; # slurp it
my @grid = map { [split('')] } split("\n", $puzzle);

(my ($min, $min_with_return)) = do_searches(\@grid);
print "P1 Solution: Shortest path to get all numbers found with distance $min.\n";
print "P2 Solution: Shortest path to get all numbers and return to start found with distance $min_with_return.\n";

sub do_searches {
	my $gridref = shift;

	# First we have to find all the numbered points to prepare the BFS portion.
	my ($x, $y);
	my %nums = ();
	my $max_num = 0;
	for ($y = 0; $y <= $#$gridref; $y++) {
		for ($x = 0; $x <= $#{$gridref->[$y]}; $x++) {
			if ($gridref->[$y][$x] =~ /\d/) {
				$nums{$gridref->[$y][$x]} = { 'x' => $x, 'y' => $y, 'd' => {} };
				$max_num = $gridref->[$y][$x] if ($gridref->[$y][$x] > $max_num);
			}
		}
	}
	print "Commencing search on map of size " . (1+$#{$gridref->[0]}) . " x " . (1+$#$gridref) . " with $max_num numbers to visit.\n";

	# Now run a BFS on each number-to-number combination
	my @pairs = combine(2, (keys %nums));
	foreach my $p (@pairs) {
		my $dist = do_BFS($gridref, $nums{$p->[0]}{'x'}, $nums{$p->[0]}{'y'}, $p->[1]);
		warn "Could not calculate distance between $p->[0] and $p->[1]\n" if ($dist < 0);
		$nums{$p->[0]}{'d'}{$p->[1]} = $dist;
		$nums{$p->[1]}{'d'}{$p->[0]} = $dist;
	}
	
	# Now check all paths. With n! possibilities this could get out of hand quickly with more spots to visit.
	# This is also where we make the assumption of no missing numbers.
	my @paths = permute(1 .. $max_num);
	my $min_dist = 1e9;
	my $min_dist_with_return = 1e9;
	foreach my $p (@paths) {
		my $this_dist = 0;
		my $current = 0;
		while (my $next = shift @$p) {
			$this_dist += $nums{$current}{'d'}{$next};
			$current = $next;
		}
		$min_dist = $this_dist if ($this_dist < $min_dist);
		# return to start
		$this_dist += $nums{$current}{'d'}{0};
		$min_dist_with_return = $this_dist if ($this_dist < $min_dist_with_return);
	}
	return $min_dist, $min_dist_with_return;
}
	
sub do_BFS {
	my $gridref = shift;
	my $start_x = shift;
	my $start_y = shift;
	my $end_num = shift;

	my $x = $start_x;
	my $y = $start_y;
	my $dist = 0;
	my %visited = ( "$x,$y" => '' );
	my @queue = ( {'x'=>$start_x, 'y'=>$start_y, 'd'=>$dist} );
	my @offsets = ( [-1, 0], [1, 0], [0, -1], [0, 1] );

	while (my $p = shift @queue) {
		return $p->{'d'} if ($gridref->[$p->{'y'}][$p->{'x'}] eq $end_num);
		$dist = $p->{'d'} + 1;
		foreach my $off (@offsets) {
			$x = $p->{'x'} + $off->[0];
			$y = $p->{'y'} + $off->[1];
			if (not exists $visited{"$x,$y"} and $gridref->[$y][$x] ne '#') {
				push @queue, {'x'=>$x, 'y'=>$y, 'd'=>$dist};
				$visited{"$x,$y"} = '';
			}
		}
	}
	return -1;
}



